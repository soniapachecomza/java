public class Main
{
    public static void main(String[] args) {

     // ciclo for 
     // for (Inicialización; Condición; Iteración){}
     
     for(int numero = 1; numero  < 101;){
         
        if( numero % 2 == 0) {
            System.out.println(numero);
        }
        
        numero++;
     }
   }
}

